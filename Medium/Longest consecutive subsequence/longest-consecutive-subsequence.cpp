class Solution{
  public:
    // arr[] : the input array
    // N : size of the array arr[]
    
    //Function to return length of longest subsequence of consecutive integers.
    int findLongestConseqSubseq(int arr[], int N)
    {
      //Your code here
      int SIZE=100001,count=0,len=0;
        vector<int> v(SIZE,0);
        for(int i=0;i<N;i++)
            v[arr[i]]=1;
        
        for(int i=0;i<=SIZE;i++)
            if(v[i]==1)
                count++;
            else
                len=max(len,count),count=0;
        return len;
    }
};

