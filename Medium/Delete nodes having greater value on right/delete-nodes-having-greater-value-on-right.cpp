class Solution
{
    public:
    Node *compute(Node *head)
    {
        // your code goes here
        
          Node* curr=head;
        Node* prev=NULL;
        while(curr->next!=NULL){
            Node* temp=curr->next;
            while(temp->next!=NULL && temp->data<=curr->data){
                temp=temp->next;
            }
            if(temp->data>curr->data){
                if(prev==NULL){
                    curr->next=NULL;
                    curr=temp;
                    head=temp;
                }
                else{
                    prev->next=temp;
                    curr->next=NULL;
                    curr=temp;
                }
            }
            else{
                prev=curr;
                curr=curr->next;
            }
        }
        return head;
    }
    
};
   


