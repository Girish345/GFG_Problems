class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
         if(head==NULL || head->next==NULL || k==0){
          return head;
      }
        

        Node *temp=head;
        int l=0;
        
         while(temp->next!=NULL){
             l++;
             temp=temp->next;
             
         }
         l++;
         temp->next=head;
         k=k%l;
         
         
         
         while(k--){
             temp=temp->next;
         }
         head=temp->next;
         temp->next=NULL;
         
         
         return head;
          
    }
};
    


