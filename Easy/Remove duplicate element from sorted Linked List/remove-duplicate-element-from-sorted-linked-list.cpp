//Function to remove duplicates from sorted linked list.
Node *removeDuplicates(Node *head)
{
 // your code goes here
 
 if (head == nullptr || head->next == nullptr)
        return head;

    map<int, int> mpp;
    Node* prev = head;
    Node* curr = head->next;

    mpp[prev->data]++;

    while (curr != nullptr) {
        mpp[curr->data]++;

        if (mpp[curr->data] > 1) {
            Node* temp = curr;
            prev->next = curr->next;
            delete temp;
            curr = prev->next;
        } else {
            prev = curr;
            curr = curr->next;
        }
    }

    return head;
}